window.addEventListener('load', function() {
  var dynamicElement = document.querySelector('.color-box1'); var color = window.getComputedStyle(dynamicElement, null).getPropertyValue('text-shadow');
  var localElement = document.querySelector('.color1');
  var localElement2 = document.querySelector('.color1');
 
  localElement.style.textShadow = color;
  localElement2.style.textShadow = color;
 var observer = new MutationObserver(function(mutationsList, observer) { for (var mutation of mutationsList) { if (mutation.type === 'attributes' && mutation.attributeName === 'style') { var newColor = window.getComputedStyle(dynamicElement, null).getPropertyValue('text-shadow'); localElement.style.textShadow = newColor; } } });  observer.observe(dynamicElement, { attributes: true, attributeFilter: ['style'] });});

